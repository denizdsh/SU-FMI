#include <iostream>

void swap(int arr[], int idx1, int idx2) {
	int temp = arr[idx1];
	arr[idx1] = arr[idx2];
	arr[idx2] = temp;
}

int main()
{
	const int size = 10;
	int nums[size] = { 2, 10, 3, 4, 6, 7, 5, 1, 9, 8 };
	swap(nums, 0, 1);
	std::cout << nums[0];
}

